package enums;

/**
 * Repräsentiert die verschiedenen Level, die ein Spieler
 * basierend auf seinem Gesamtfortschritt erreichen kann.
 */
public enum Level {

    /**
     * Einstiegslevel für neue Spieler.
     */
    ANFÄNGER,

    /**
     * Bronze-Level – zeigt erste Fortschritte an.
     */
    BRONZE,

    /**
     * Silber-Level – mittlerer Fortschritt.
     */
    SILBER,

    /**
     * Gold-Level – fortgeschrittene Spieler.
     */
    GOLD,

    /**
     * Höchstes erreichbares Level: Master.
     */
    MASTER
}